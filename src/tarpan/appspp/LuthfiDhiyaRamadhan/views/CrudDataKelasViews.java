    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
         String inputID = txtIdkelas.getText();
         String inputNama = txtNamaKelas.getText();
         String inputKelas = cbKompetensiKeahlian.getSelectedItem().toString();

         if (txtIdkelas.getText().equals("") || txtNamaKelas.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Data Harus di isi");
         }else{
         try {
             
            Connection con = new Koneksi().konek();
            String sql = "UPDATE kelas SET nama_kelas = ?, kompetensi_keahlian =? where id_kelas =?";
            PreparedStatement ps = con.prepareStatement(sql);
            
            ps.setString(1, inputNama);
            ps.setString(2, inputKelas);
            ps.setString(3, inputID);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"Berhasil Mengedit Data");
            tampilDataKelas();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }
         }
    }//GEN-LAST:event_btnEditActionPerformed

