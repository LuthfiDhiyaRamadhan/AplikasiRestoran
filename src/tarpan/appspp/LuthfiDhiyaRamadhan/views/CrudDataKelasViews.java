    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String inputID = txtIdkelas.getText();
        String inputKelas = txtNamaKelas.getText();
        String inputKeahlian = cbKompetensiKeahlian.getSelectedItem().toString();
                
        try {
            Connection con = new Koneksi().konek();
            String sql = "INSERT INTO kelas VALUES (?,?,?)";
            PreparedStatement ps = con.prepareStatement(sql);
            
            ps.setString(1, inputID);
            ps.setString(2, inputKelas);
            ps.setString(3, inputKeahlian);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"Data Berhasil Ditambahkan");
            tampilDataKelas();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnTambahActionPerformed

