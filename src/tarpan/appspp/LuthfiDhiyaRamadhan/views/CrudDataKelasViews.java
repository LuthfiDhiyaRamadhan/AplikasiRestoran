    public void tampilDataKelas (){
        String [] judul = {"ID KELAS", "NAMA KELAS", "KOMPETENSI KEAHLIAN"};
        DefaultTableModel model;
        
        model = new DefaultTableModel(judul, 0);
        tblKelas.setModel(model);
        
        try {
            Connection con = new Koneksi().konek();
            String sql = "SELECT * FROM kelas";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()){
                String id = rs.getString ("id_kelas");
                String nama = rs.getString("nama_kelas");
                String kompetensi = rs.getString("kompetensi_keahlian");
                
                String [] data = {id, nama, kompetensi };
                model.addRow(data);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }
                
                
    }
        
    

