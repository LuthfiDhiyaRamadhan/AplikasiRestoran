    public void tampilDataPetugas (){
        String [] judul = {"ID PETUGAS", "USERNAME", "PASSWORD", "NAMA_PETUGAS","LEVEL"};
        DefaultTableModel model;
        
        model = new DefaultTableModel(judul, 0);
        tblPetugas.setModel(model);
        
        try {
            Connection con = new Koneksi().konek();
            String sql = "SELECT * FROM petugas";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()){
                String id = rs.getString ("id_petugas");
                String username = rs.getString("username");
                String password = rs.getString("password");
                String nama_petugas = rs.getString("nama_petugas");
                String level = rs.getString("level");
                
                String [] data = {id, username, password, nama_petugas, level };
                model.addRow(data);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }
                
                
    }
    
