            PreparedStatement ps = con.prepareStatement(sql);
            
           
            ps.setString(1, inputUsername);
            ps.setString(2, inputPassword);
            ps.setString(3, inputNama_Petugas);
            ps.setString(4, inputLevel);
             ps.setString(5, inputID);
             
