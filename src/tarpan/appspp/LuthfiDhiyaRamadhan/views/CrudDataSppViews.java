    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String inputID = txtIdSpp.getText();
        String inputTahun = txtTahun.getText();
        String inputNominal= txtNominal.getText();
     
                
        try {
            Connection con = new Koneksi().konek();
            String sql = "INSERT INTO spp VALUES (?,?,?)";
            PreparedStatement ps = con.prepareStatement(sql);
            
            ps.setString(1, inputID);
            ps.setString(2, inputTahun);
            ps.setString(3, inputNominal);
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"Data Berhasil Ditambahkan");
            tampilDataSpp();
        } catch (Exception e) {
        }
                                             
                                             
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String inputID = txtIdSpp.getText();
         String inputTahun = txtTahun.getText();
         String inputNominal = txtNominal.getText();

         if (txtIdSpp.getText().equals("") || txtTahun.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Data Harus di isi");
         }else{
         try {
             
            Connection con = new Koneksi().konek();
            String sql = "UPDATE spp SET tahun = ?, nominal =? where id_spp =?";
            PreparedStatement ps = con.prepareStatement(sql);
            
            ps.setString(1,inputTahun);
            ps.setString(2, inputNominal);
            ps.setString(3, inputID);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"Berhasil Mengedit Data");
            tampilDataSpp();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }
         }
    }//GEN-LAST:event_btnEditActionPerformed

