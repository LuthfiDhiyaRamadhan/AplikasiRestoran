    public void tampilDataSpp (){
        String [] judul = {"ID SPP", "TAHUN","NOMINAL"};
        DefaultTableModel model;
        
        model = new DefaultTableModel(judul, 0);
        tblSpp.setModel(model);
        
        try {
            Connection con = new Koneksi().konek();
            String sql = "SELECT * FROM spp";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()){
                String id = rs.getString ("id_spp");
                String tahun = rs.getString("tahun");
                String nominal = rs.getString("nominal");
                
                
                String [] data = {id, tahun, nominal};
                model.addRow(data);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }
                
                
    }

