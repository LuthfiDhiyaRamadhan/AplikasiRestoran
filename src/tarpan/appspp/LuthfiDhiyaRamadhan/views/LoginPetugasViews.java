    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
       // TODO add your handling code here:
       String user = txtUsername.getText();
       String pass = String.valueOf(txtPassword.getText());
       
        try {
            if (txtUsername.getText().equals("") || txtPassword.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Harus di isi");
         }else{
                Connection con = new Koneksi().konek();
            String sql = "SELECT * FROM petugas where `username` =? AND `password` = ?";
            PreparedStatement ps = con.prepareStatement(sql);
            
            ps.setString(1, user);
            ps.setString(2, pass);
            
            ResultSet rs = ps.executeQuery();
            if (rs.next()){
                String id = rs.getString("id_petugas");
                String username = rs.getString("username");
                String nama = rs.getString("nama_petugas");
                String level = rs.getString("level");
                
                UserSession.setId(id);
                UserSession.setUsername(username);
                UserSession.setNama(nama);
                UserSession.setLevel(level);
                
            if (level.equals("admin")){
                JOptionPane.showMessageDialog(this,"Hallo Selamat Datang"+ nama);
                DashboardViews dashboard = new DashboardViews();
                dashboard.dashAdmin();
                dashboard.setVisible(true);
                this.dispose();
            
            } else if (level.equals("petugas")){
                JOptionPane.showMessageDialog(this,"Hallo Selamat Datang"+ nama);
                DashboardViews dashboard = new DashboardViews();
                dashboard.dashPetugas();
                dashboard.setVisible(true);
                this.dispose();
            }
            
            }
            else{
               JOptionPane.showMessageDialog(this,"Username dan Password salah");
            }
            }
            
        } catch (Exception e) {
            
        }
    }//GEN-LAST:event_btnLoginActionPerformed

